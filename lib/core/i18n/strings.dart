/// 集中式字符串字典。
///
/// - key:点号分隔路径(如 `home.developerIntelligence`)。
/// - 缺失时回退到中文,再回退到 key 本身,确保 UI 永远不崩。
class AppStrings {
  const AppStrings(this.locale);

  final AppLocale locale;

  String t(String key) {
    final byLocale = _data[locale];
    final value = byLocale?[key];
    if (value != null) return value;
    final fallback = _data[AppLocale.zh]?[key];
    if (fallback != null) return fallback;
    return key;
  }

  /// 翻译带占位符 `$name` / `$hours` 的字符串,缺失占位符时回退到 `t(key)`。
  String tr(String key, Map<String, Object> params) {
    final template = t(key);
    var out = template;
    params.forEach((name, value) {
      out = out.replaceAll('\$' + name, value.toString());
    });
    return out;
  }

